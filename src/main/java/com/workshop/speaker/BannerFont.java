    private static void put(char c, String... rows) { GLYPHS.put(c, rows); }

    /** Render `text` as a multi-line ASCII banner. */
    static String render(String text) {
        String upper = text.toUpperCase();
        StringBuilder[] rows = new StringBuilder[ROWS];
        for (int i = 0; i < ROWS; i++) rows[i] = new StringBuilder();

        for (int i = 0; i < upper.length(); i++) {
            char c = upper.charAt(i);
            String[] glyph = GLYPHS.getOrDefault(c, GLYPHS.get('?'));
            for (int r = 0; r < ROWS; r++) {
                if (i > 0) rows[r].append(' ');
                rows[r].append(glyph[r]);
            }
        }
        StringBuilder out = new StringBuilder();
        for (StringBuilder row : rows) out.append(row).append('\n');
        return out.toString();
    }

